    private void jButtonRunKNNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRunKNNActionPerformed
        
        if(isCalculating){
            isCalculating = false;
            knn.running = false;
            jButtonRunKNN.setText("Run KNN");
        }else{
            isCalculating = true;
            jButtonRunKNN.setText("Stop KNN");
            jTotalTimeText.setVisible(false);
            jTotalTimeLabel.setVisible(false);
            jEstimatedTimeText.setForeground(Color.black);
            ShowProgressPanel(true);
            try{
                K = Integer.parseInt(jText_KNeighbours.getText());
                if(K>k.unknownNumbersAmount) throw new NumberFormatException();
            }
            catch(NumberFormatException e){
                if(K > k.unknownNumbersAmount){
                    jText_KNeighbours.setText(Integer.toString(k.unknownNumbersAmount));
                    K = k.unknownNumbersAmount;
                }
                else {
                    jText_KNeighbours.setText("5");
                    K = 5;
                }
            }
            metricType = jMetricComboBox.getSelectedIndex();
            System.out.println("Metric: " + metricType );
            knn = new KNN();


            final Timer t = new Timer(250, new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent e) {
                    float progress = knn.GetProgress();
                    long tmpTime = System.currentTimeMillis() - start_time;
                    tmpTime = (long) (tmpTime * 100.0/progress-tmpTime);
                    minutes = TimeUnit.MILLISECONDS.toMinutes(tmpTime);
                    tmpTime -= TimeUnit.MINUTES.toMillis(minutes);
                    seconds = TimeUnit.MILLISECONDS.toSeconds(tmpTime);
                    String estimatedTime = (minutes + "m " + seconds + "s");
                    if(minutes<1000) SetProgress((int)progress, estimatedTime);
                    else SetProgress((int)progress, "?");
                    if(jProgressBar.getValue() == 100){
                        end_time = System.currentTimeMillis() - start_time;
                        minutes = TimeUnit.MILLISECONDS.toMinutes(end_time);
                        end_time -= TimeUnit.MINUTES.toMillis(minutes);
                        seconds = TimeUnit.MILLISECONDS.toSeconds(end_time);
                        String totalTime = (minutes + "m " + seconds + "s");
                        SetProgress(100, "Finished");
                        jEstimatedTimeText.setForeground(new Color(32,164,32));
                        jButtonRunKNN.setText("Run KNN");
                        jTotalTimeText.setText(totalTime);
                        jTotalTimeText.setVisible(true);
                        jTotalTimeLabel.setVisible(true);
                        isCalculating = false;
                        ShowResults(knn.result, knn.resultPerNumber);
                        ((Timer) e.getSource()).stop();
                    }else if(isCalculating == false){
                        end_time = System.currentTimeMillis() - start_time;
                        minutes = TimeUnit.MILLISECONDS.toMinutes(end_time);
                        end_time -= TimeUnit.MINUTES.toMillis(minutes);
                        seconds = TimeUnit.MILLISECONDS.toSeconds(end_time);
                        String totalTime = (minutes + "m " + seconds + "s");
                        jEstimatedTimeText.setText("Aborted");
                        jEstimatedTimeText.setForeground(Color.red);
                        jButtonRunKNN.setText("Run KNN");
                        jTotalTimeText.setText(totalTime);
                        jTotalTimeText.setVisible(true);
                        jTotalTimeLabel.setVisible(true);
                        ((Timer) e.getSource()).stop();
                    }
                }
            });

            Thread t1 = new Thread(new Runnable() {

                @Override
                public void run() {
                   while(isCalculating && knn.progress<100){
                        knn.RunKNN(k.unknownFeatures, k.trainingFeatures, K, metricType, k.classesAmount);
                   }
                }
            });

            start_time = System.currentTimeMillis();
            t.start();
            t1.start();
        }
        
        
    }//GEN-LAST:event_jButtonRunKNNActionPerformed

