    void ColorizeResult(JLabel label, float value)
    {
        if(value>90) {
            label.setForeground(new Color(32,164,32));
        }else if(value<70){
             label.setForeground(Color.red);
        }else{
            label.setForeground(Color.black);
        }
    }
   
    
